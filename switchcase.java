public class switchcase {
    public static void main(String args[]){
       int score = 1;
       switch(score){
        case 1: System.out.println("over 0.5");
        break;
        case 2: System.out.println("over 1.5");
        break;
        case 3: System.out.println("over 2.5");
        default: System.out.println("loser");
       }
    }    
}
